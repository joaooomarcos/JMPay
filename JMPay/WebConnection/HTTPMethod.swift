import Alamofire

enum HTTPMethod {
    
    case get
    case post
    
    func toAlamofire() -> Alamofire.HTTPMethod {
        switch self {
        case .get:
            return Alamofire.HTTPMethod.get
        case .post:
            return Alamofire.HTTPMethod.post
        }
    }
}
